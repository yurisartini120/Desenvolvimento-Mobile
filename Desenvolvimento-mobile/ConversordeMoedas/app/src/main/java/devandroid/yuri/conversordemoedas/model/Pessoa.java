package devandroid.yuri.conversordemoedas.model;

public class Pessoa {
    public double libra(double valor){
        return valor * 6.00;

    }

    public double peso(double valor){
        return valor *  0.014 ;

    }

    public  double dolar(double valor){
        return valor * 4.91 ;

    }

    public  double euro(double valor){
        return valor *  5.24 ;

    }


}
